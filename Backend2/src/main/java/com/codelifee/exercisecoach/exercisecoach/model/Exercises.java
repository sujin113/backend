package com.codelifee.exercisecoach.exercisecoach.model;

public class Exercises {

	private int exercies_sequence_id;
	private String exercise_name;
	private String descriptions;
	
	public int getExercies_sequence_id() {
		return exercies_sequence_id;
	}
	public void setExercies_sequence_id(int exercies_sequence_id) {
		this.exercies_sequence_id = exercies_sequence_id;
	}
	public String getExercise_name() {
		return exercise_name;
	}
	public void setExercise_name(String exercise_name) {
		this.exercise_name = exercise_name;
	}
	public String getDescriptions() {
		return descriptions;
	}
	public void setDescriptions(String descriptions) {
		this.descriptions = descriptions;
	}	
}